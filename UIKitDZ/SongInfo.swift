/// SongInfo - Информация о треке
/// name - Название трека
/// time - Время трека
/// image - Фото песни
/// artist - Исполнитель песни
/// trackName - название песни которое мы загрузили
struct SongInfo {
    let name: String
    let time: Int
    let image: String
    let artist: String
    let trackName: String
}
